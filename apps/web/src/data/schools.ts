export function getSchoolsByTier(tier: SchoolTier): School[] {
  return SCHOOLS.filter((s) => s.tier === tier);
}

export function getSchoolsByType(type: SchoolType): School[] {
  return SCHOOLS.filter((s) => s.type === type);
}

export function getSchoolsByTrack(track: string): School[] {
  return SCHOOLS.filter((s) => s.tracks.includes(track));
}

export function getSchoolsByCity(city: string): School[] {
  return SCHOOLS.filter((s) => s.city === city || s.cities?.includes(city));
}

export function getSchoolBySlug(slug: string): School | undefined {
  return SCHOOLS.find((s) => s.slug === slug);
}

export function getTopSchoolsByTrack(track: string, limit = 5): School[] {
  const tierOrder: SchoolTier[] = ["elite", "premium", "selective", "standard", "accessible"];
  return SCHOOLS
    .filter((s) => s.tracks.includes(track))
    .sort((a, b) => tierOrder.indexOf(a.tier) - tierOrder.indexOf(b.tier))
    .slice(0, limit);
}

